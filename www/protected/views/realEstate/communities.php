<h3><span id="ContentPlaceHolder1_lbl_title">Community Neighborhoods</span></h3>
<p id="ContentPlaceHolder1_span_community"></p><p></p><p><font id="text">The City of Chicago has 77 officially defined
    <a href="http://www.chicagotribune.com/classified/realestate/communities/chi-communities,0,7944296.htmlstory#com">community
        areas</a>. Many of their names will be instantly recognizable. Others aren't much used outside of official
    pronouncements. Instead, people use a traditional or informal <a
        href="http://www.chicagotribune.com/classified/realestate/communities/chi-communities,0,7944296.htmlstory#neigh">neighborhood
        name</a>, whether Streeterville, Wicker Park or Back of the Yards. </font></p>
<p><font id="text">
    </font></p><p><font id="text">Because census and much real estate data map to the official community names, we have
    used those areas to organize information on this site. That means that if you enter a neighborhood name, you
    will generally get data for the broader community of which it is a part.</font></p><font id="text">
